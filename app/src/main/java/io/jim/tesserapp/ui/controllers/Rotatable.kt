package io.jim.tesserapp.ui.controllers

/**
 * A rotatable object.
 */
interface Rotatable {

    /**
     * X-Rotation.
     */
    var x: Double

    /**
     * Y-Rotation.
     */
    var y: Double

    /**
     * Z-Rotation.
     */
    var z: Double

    /**
     * Q-Rotation.
     */
    var q: Double
}
