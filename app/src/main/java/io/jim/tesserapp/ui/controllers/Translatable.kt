package io.jim.tesserapp.ui.controllers

/**
 * A translatable object.
 */
interface Translatable {

    /**
     * X-Translation.
     */
    var x: Double

    /**
     * Y-Translation.
     */
    var y: Double

    /**
     * Z-Translation.
     */
    var z: Double

    /**
     * Q-Translation.
     */
    var q: Double
}
